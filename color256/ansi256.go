const (
	foregroundFmt = "38;5;%d"
	backgroundFmt = "48;5;%d"
)

// Foreground returns the ANSI escape code for setting the foreground color
// to the Color value. The color is specified using the extended ANSI foreground
// colors.
func (c Color) Foreground() string {
	return fmt.Sprintf(foregroundFmt, c&0xff)
}

// Background returns the ANSI escape code for setting the background color
// to the Color value. The color is specified using the extended ANSI background
// colors.
func (c Color) Background() string {
	return fmt.Sprintf(backgroundFmt, c&0xff)
}
