const (
	foregroundFmt = "38;2;%d;%d;%d"
	backgroundFmt = "48;2;%d;%d;%d"
)

// RGB returns the red, green, and blue components of the Color.
// Each component is an integer between 0 and 255.
func (c Color) RGB() (int, int, int) {
	i := int(c)
	return (i >> 16) & 255, (i >> 8) & 255, i & 255
}

// RGB creates a Color from red, green, and blue components.
// Each component should be an integer between 0 and 255.
func RGB(r, g, b int) Color {
	return Color(((r & 255) << 16) | ((g & 255) << 8) | (b & 255))
}

// Hex creates a Color from a hexadecimal string.
// The string should start with '#' and be followed by six hexadecimal digits.
// If the string is not in the correct format, the function returns 0.
func Hex(hex string) Color {
	if hex[0] != '#' {
		return 0
	}
	switch len(hex) {
	case 7:
		v, err := strconv.ParseInt(hex[1:], 16, 32)
		if err != nil {
			return 0
		}
		return Color(v)
	case 4:
		v, err := strconv.ParseInt(hex[1:], 16, 32)
		if err != nil {
			return 0
		}
		r, g, b := (v>>8)&0xF, (v>>4)&0xF, v&0xF
		v = 0
		v |= ((r << 4) | r) << 16
		v |= ((g << 4) | g) << 8
		v |= ((b << 4) | b)
		return Color(v)
	default:
		return 0
	}
}

// Foreground returns the ANSI escape code for setting the foreground color
// to the Color value. The color is specified using 24-bit RGB values.
func (c Color) Foreground() string {
	r, g, b := c.RGB()
	return fmt.Sprintf(foregroundFmt, r, g, b)
}

// Background returns the ANSI escape code for setting the background color
// to the Color value. The color is specified using 24-bit RGB values.
func (c Color) Background() string {
	r, g, b := c.RGB()
	return fmt.Sprintf(backgroundFmt, r, g, b)
}
